#include <LPC17xx.h>

void Fc_config_pines(void){
	LPC_PINCON->PINSEL0=(LPC_PINCON->PINSEL0&~((0x3<<8)|(0x3<<2)|(0x3<<6)|(0x3)|(0x3<<4)))|(0x3<<8)|(0x2<<2)|(0x1<<6)|(0x2)|(0x1<<4);	// CAP2.0 PIN0.4, RXD0 PIN0.3, RXD3 PIN0.1, TXD3 PIN0.0
	LPC_PINCON->PINSEL1=(0x3<<14);	//	CAP3.0 PIN0.23
	LPC_PINCON->PINSEL2&=~0xF;	//	BIT 3:0 GPIO PIN 1.0 AND 1.1
	LPC_PINCON->PINSEL3=(LPC_PINCON->PINSEL3&~((0x3<<8)|(0x3F)|(0x3<<14)))|(0x2<<8)|(0x3<<4)|(0x2<<14);	// CLEAR BIT 15:14/9:8/7:0, PWM1.4 PIN1.23 PWM1.2 PIN1.20, PIN 1.16/1.17 GPIO AND CAP1.0 PIN 1.18
	LPC_PINCON->PINSEL4=(LPC_PINCON->PINSEL4&(~(0xF<<22)))|(0x5<<22);	 //	CLEAR BITS 23:22, EINT1 PIN2.11, EINT2 PIN2.12
	
	LPC_GPIO1->FIODIR|=(0x3)|(0x3)<<16;	//	PIN 1.0 AND 1.1/ PIN 1.16 AND 1.17 OUTPUT
}
